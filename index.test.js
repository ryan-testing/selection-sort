const main = require('./index')

test("[ 33, 2, 52, 106, 73 ] -> [ 2, 33, 52, 73, 106 ]", () => {
  expect(main([33, 2, 52, 106, 73])).toEqual([2, 33, 52, 73, 106]);
});

test("[ 13, 5, 22, 99, 11 ] -> [ 5, 11, 13, 22, 99 ]", () => {
  expect(main([13, 5, 22, 99, 11])).toEqual([5, 11, 13, 22, 99]);
});

test("[ 12, 5, 6, 3, 9 ] -> [ 3, 5, 6, 9, 12 ]", () => {
  expect(main([12, 5, 6, 3, 9])).toEqual([3, 5, 6, 9, 12]);
});
